package com.synopsys.helpers;

/**
 * Helper class that handles mathematical operations.
 * 
 * @author Neeta
 *
 */
public class CalcOperationsHelper {
	
	/**
	 * Performs addition
	 * 
	 * @param numOne
	 * @param numTwo
	 * @return int value
	 */
	public static int add(int numOne, int numTwo) {
		return numOne + numTwo;
	}

	/**
	 * Performs subtraction
	 * 
	 * @param numOne
	 * @param numTwo
	 * @return int value
	 */
	public static int sub(int numOne, int numTwo) {
		return numOne - numTwo;
	}
	
	/**
	 * Performs multiplication
	 * 
	 * @param numOne
	 * @param numTwo
	 * @return int value
	 */
	public static int mult(int numOne, int numTwo) {
		return numOne * numTwo;
	}
	
	/**
	 * Performs division
	 * 
	 * @param numOne
	 * @param numTwo
	 * @return int value
	 */
	public static int div(int numOne, int numTwo) {
		return numOne/numTwo;
	}
}
